def sum(a, b):
    """Return the sum of a and b

    Args:
        a (int)
        b (int)

    Returns:
        Sum of a and b (int)
    """
    return a + b


def subtraction(a, b):
    """Return the substraction of a and b

    Args:
        a (int)
        b (int)

    Returns:
        Substraction of a and b (int)
    """
    return a - b
